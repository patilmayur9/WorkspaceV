//Write the program to check whether the no is greater than 100.
public class NumIsGreaterThan100
{
	public static void main(String args[])
	{
		int i=10;
		if(i>100)
		{
			System.out.println("The given no is GREATER than 100 and the given no is : "+i);
			
		}
		else
		{
			System.out.println("The given no is NOT greater than 100 and the given no is : "+i);
		}
	}
}
